    private void addexpenseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addexpenseButtonActionPerformed
        // TODO add your handling code here:
        Money money = new Money();
        money.setDate(jDateChooser2.getDate());
        money.setMoney(Integer.parseInt(expenseTextField.getText()));
        money.setCategory((String) jComboBox1.getSelectedItem());
        money.setNote(noteexpenseTextField.getText());
        
        expense += Integer.parseInt(expenseTextField.getText());
        jTextField1.setText(String.valueOf(expense));
        
        int a = income - expense;
        jTextField2.setText(String.valueOf(a));
        
        list.add(money);
        showResult();
    }//GEN-LAST:event_addexpenseButtonActionPerformed

