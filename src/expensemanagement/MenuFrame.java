    public void showResult2(){
        Money money = list.get(list.size()-1);
        model.addRow(new Object[]{
            money.getDate(), money.getCategory(), money.getMoney(), money.getNote()
        });
    }
    
    public void showResult(){
        Money money = list.get(list.size()-1);
        model.addRow(new Object[]{
            money.getDate(), money.getCategory(), (-1)*money.getMoney(), money.getNote()
        });
    }
    
